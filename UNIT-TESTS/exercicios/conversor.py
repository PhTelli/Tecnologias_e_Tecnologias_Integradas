def dolar_para_euro(valor, taxa_dolar_euro):
    return valor * taxa_dolar_euro

def euro_para_dolar(valor, taxa_euro_dolar):
    return valor * taxa_euro_dolar

def real_para_dolar(valor, taxa_real_dolar):
    return valor * taxa_real_dolar

def dolar_para_real(valor, taxa_dolar_real):
    return valor * taxa_dolar_real